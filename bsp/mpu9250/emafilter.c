int16_t emaFilter_LowPass(int16_t new, int16_t last, uint8_t alpha)
{
	int16_t out;

    if(alpha == 0)
        return new;

    out = (new / alpha);
    out += last;
    out -= (last / alpha);
    return out;
}
 
int16_t emaFilter_HighPass(int16_t new, int16_t last, uint8_t alpha)
{
    if(alpha == 0)
        return new;

    last = new - emaFilter_LowPass(new, last, alpha);
    return last;
}

